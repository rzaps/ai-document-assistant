from reportlab.pdfbase.ttfonts import TTFont
from reportlab.lib.enums import TA_CENTER, TA_LEFT

OUTPUT_DIR = Path(__file__).parent / "demo_docs"
OUTPUT_DIR.mkdir(exist_ok=True)

# Регистрируем системный шрифт с поддержкой кириллицы
# На Windows используем Arial из системы
import os, sys

def get_cyrillic_font():
    """Ищет подходящий шрифт с кириллицей."""
    candidates = [
        # Windows
        r"C:\Windows\Fonts\arial.ttf",
        r"C:\Windows\Fonts\times.ttf",
        # Linux
        "/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf",
        "/usr/share/fonts/truetype/liberation/LiberationSans-Regular.ttf",
        # macOS
        "/Library/Fonts/Arial.ttf",
        "/System/Library/Fonts/Helvetica.ttc",
    ]
    for path in candidates:
        if os.path.exists(path):
            return path
    return None


font_path = get_cyrillic_font()
if font_path:
    pdfmetrics.registerFont(TTFont("CyrFont", font_path))
    FONT_NAME = "CyrFont"
else:
    # Fallback — встроенный шрифт (без кириллицы, но не упадёт)
    FONT_NAME = "Helvetica"


def make_pdf(filename: str, title: str, sections: list):
    """Создаёт PDF с заголовком и секциями."""
    filepath = str(OUTPUT_DIR / filename)
    doc = SimpleDocTemplate(
        filepath,
        pagesize=A4,
        leftMargin=2 * cm,
        rightMargin=2 * cm,
        topMargin=2 * cm,
        bottomMargin=2 * cm,
    )

    title_style = ParagraphStyle(
        "Title",
        fontName=FONT_NAME,
        fontSize=16,
        alignment=TA_CENTER,
        spaceAfter=12,
        leading=20,
    )
    heading_style = ParagraphStyle(
        "Heading",
        fontName=FONT_NAME,
        fontSize=12,
        spaceBefore=10,
        spaceAfter=4,
        leading=16,
    )
    body_style = ParagraphStyle(
        "Body",
        fontName=FONT_NAME,
        fontSize=10,
        spaceAfter=6,
        leading=14,
    )

    story = [Paragraph(title, title_style), Spacer(1, 0.3 * cm)]

    for section_title, body in sections:
        story.append(Paragraph(section_title, heading_style))
        # Заменяем \n на <br/> для reportlab
        body_html = body.replace("\n", "<br/>")
        story.append(Paragraph(body_html, body_style))
        story.append(Spacer(1, 0.2 * cm))

    doc.build(story)
    print(f"Создан: {filepath}")


